package dus;

public class OrderListArray {
	
	String name="";
	int no=0;
	int price=0;
	int count=0;
	int total=0;

	public OrderListArray(String name, int no, int price, int count, int total)
	{
		this.name = name;
		this.no = no;
		this.price = price;
		this.count = count;
		this.total = total;
	}
	
}
